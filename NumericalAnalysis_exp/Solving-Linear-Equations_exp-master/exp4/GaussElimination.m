% Gauss 消元

function y = GaussElimination()
M = [3, -2, 1, 4; -7, 5, -3, -6; 2, 1, -1, 3; 4, -3, 2, 8]

% 化为上三角形式
M(2,:) = M(2,:) - M(1,:)/M(1,1)*M(2,1);
M(3,:) = M(3,:) - M(1,:)/M(1,1)*M(3,1) - M(2,:)/M(2,1)*M(3,2);
M(4,:) = M(4,:) - M(1,:)/M(1,1)*M(4,1) - M(2,:)/M(2,1)*M(4,2) - M(3,:)/M(3,1)*M(4,3);

A = M(1,1) * M(2,2) * M(3,3) * M(4,4);

% 输出结果
fprintf('行列式为%f', A);

end